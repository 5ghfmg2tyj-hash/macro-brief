// ==============================================================================
// app.js — wire up tabs, data loading, live-data grid, and brief reader.
// ==============================================================================

(function () {
  const vEl = document.getElementById("app-version");
  if (vEl && window.macroBrief?.version) vEl.textContent = `v${window.macroBrief.version}`;

  const state = {
    history:    null,
    flows:      null,
    dailyFlows: null,
    briefs:     null,  // { briefs: [{ slug, title, date }...] }
    liveLoaded: false
  };

  // ---------- tabs ----------
  function initTabs() {
    const tabs   = document.querySelectorAll(".tab");
    const panels = document.querySelectorAll(".tab-panel");
    tabs.forEach(t => {
      t.addEventListener("click", () => {
        const key = t.dataset.tab;
        tabs.forEach(x => x.classList.toggle("active", x === t));
        panels.forEach(p => p.classList.toggle("active", p.id === `tab-${key}`));

        // lazy-load live data on first visit
        if (key === "live" && !state.liveLoaded) refreshLive();
      });
    });
  }

  // ---------- data load ----------
  async function loadJSON(path) {
    const r = await fetch(path, { cache: "no-store" });
    if (!r.ok) throw new Error(`${path}: HTTP ${r.status}`);
    return r.json();
  }

  async function loadDailyFlows() {
    const url = window.macroBrief?.dailyFlowsUrl;
    if (!url) return null;
    try { const r = await fetch(url, { cache: "no-store" }); return r.ok ? r.json() : null; }
    catch { return null; }
  }

  async function loadAll() {
    try {
      const [history, flows, dailyFlows] = await Promise.all([
        loadJSON("data/history.json"),
        loadJSON("data/flows.json"),
        loadDailyFlows(),
      ]);
      state.history    = history;
      state.flows      = flows;
      state.dailyFlows = dailyFlows;
      window.Chart.init({ history, flows, dailyFlows });
    } catch (e) {
      document.getElementById("subtitle").textContent =
        "Failed to load data: " + e.message;
    }

    try {
      state.briefs = await loadJSON("briefs/index.json");
      loadLatestBrief();
      renderBriefHistory();
    } catch (e) {
      document.getElementById("brief-body").innerHTML =
        `<div class="muted">Failed to load briefs index: ${e.message}</div>`;
    }
  }

  // ---------- live data ----------
  async function refreshLive() {
    if (!state.history) return;
    state.liveLoaded = true;
    const grid = document.getElementById("live-grid");
    const status = document.getElementById("live-status");
    const btn = document.getElementById("refresh-live");
    btn.disabled = true;
    status.textContent = "Loading…";

    try {
      const live = await window.Live.load();
      const assets = live.assets || {};
      const hasAny = Object.keys(assets).length > 0;
      if (!hasAny) {
        grid.innerHTML = `<div class="muted" style="grid-column:1/-1">
          <strong>No live data yet.</strong><br>
          <code>data/live.json</code> is still the placeholder committed with
          the initial scaffold. The <em>Refresh live market data</em> GitHub
          Action has not run yet — open the repo's Actions tab and dispatch it
          once, then reload this page.
        </div>`;
      } else {
        grid.innerHTML = state.history.assets
          .map(a => renderLiveCardHTML(a, assets[a.key] || { error: "missing from live.json" }))
          .join("");
      }
      const ts = live.fetchedAt ? new Date(live.fetchedAt) : null;
      document.getElementById("live-ts").textContent =
        ts ? `${ts.toLocaleString()} (pre-fetched server-side)` : "never";
    } catch (e) {
      grid.innerHTML = `<div class="muted" style="grid-column:1/-1">
        Failed to load <code>data/live.json</code>: ${e.message}.<br>
        This file is generated by the <code>Refresh live market data</code>
        GitHub Action. If this is a brand-new deploy, trigger it once from the
        Actions tab and reload the page.
      </div>`;
    }

    status.textContent = "";
    btn.disabled = false;
  }

  function renderLiveCardHTML(asset, result) {
    if (result.error) {
      return `<div class="live-card err" id="live-${asset.key}">
        <div class="live-label">${asset.label}</div>
        <div class="live-idx">${asset.idx}</div>
        <div class="live-val">Error</div>
        <div class="live-meta"><span>${result.error}</span></div>
      </div>`;
    }
    const histLast = asset.vals[asset.vals.length - 1];
    const delta = histLast ? ((result.value - histLast) / histLast * 100) : null;
    const dCls  = delta === null ? "" : (delta > 0 ? "delta-up" : "delta-dn");
    const deltaTxt = delta !== null
      ? `${delta > 0 ? "+" : ""}${delta.toFixed(2)}% vs last brief`
      : "";
    return `<div class="live-card" id="live-${asset.key}">
      <div class="live-label">${asset.label}</div>
      <div class="live-idx">${asset.idx}</div>
      <div class="live-val">${formatLiveValue(result.value, asset.unit)}</div>
      <div class="live-meta">
        <span>${result.asOf || "—"} · ${result.source || ""}</span>
        <span class="${dCls}">${deltaTxt}</span>
      </div>
    </div>`;
  }

  function formatLiveValue(v, unit) {
    if (v === null || v === undefined || isNaN(v)) return "—";
    switch (unit) {
      case "pts": return Math.round(v).toLocaleString("en-US");
      case "usd": return "$" + v.toLocaleString("en-US", { maximumFractionDigits: v < 100 ? 2 : 0 });
      case "bbl": return "$" + v.toFixed(v < 100 ? 2 : 0) + "/bbl";
      case "oz":  return "$" + Math.round(v).toLocaleString("en-US") + "/oz";
      case "bps": return Math.round(v) + " bps";
      case "pct": return v.toFixed(2) + "%";
      default:    return String(v);
    }
  }

  // ---------- briefs ----------
  async function loadLatestBrief() {
    if (!state.briefs || !state.briefs.briefs?.length) {
      document.getElementById("brief-body").innerHTML =
        '<div class="muted">No briefs available yet.</div>';
      return;
    }
    const latest = state.briefs.briefs[0];
    await loadBrief(latest);
  }

  async function loadBrief(b) {
    document.getElementById("brief-title").textContent = b.title || b.slug;
    document.getElementById("brief-sub").textContent   = b.date || "";
    try {
      const r = await fetch(`briefs/${b.slug}.md`, { cache: "no-store" });
      if (!r.ok) throw new Error(`HTTP ${r.status}`);
      const md = await r.text();
      document.getElementById("brief-body").innerHTML = renderMarkdown(md);
    } catch (e) {
      document.getElementById("brief-body").innerHTML =
        `<div class="muted">Failed to load brief: ${e.message}</div>`;
    }
  }

  function renderBriefHistory() {
    const ul = document.getElementById("brief-history");
    if (!ul || !state.briefs) return;
    ul.innerHTML = state.briefs.briefs.map(b => `
      <li>
        <a href="#" data-slug="${b.slug}">${b.title || b.slug}</a>
        <span class="muted">${b.date || ""}</span>
      </li>`).join("");
    ul.querySelectorAll("a").forEach(a => {
      a.addEventListener("click", e => {
        e.preventDefault();
        const slug = a.dataset.slug;
        const b = state.briefs.briefs.find(x => x.slug === slug);
        if (b) loadBrief(b);
        window.scrollTo({ top: 0, behavior: "smooth" });
      });
    });
  }

  // ---------- minimal markdown ----------
  // Supports: headings, paragraphs, bold/italic, inline code, code fences,
  // unordered + ordered lists, blockquotes, links, horizontal rules, tables.
  function renderMarkdown(md) {
    const esc = s => s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
    const inline = s => s
      .replace(/`([^`]+)`/g, (_, t) => `<code>${esc(t)}</code>`)
      .replace(/\*\*([^*]+)\*\*/g, "<strong>$1</strong>")
      .replace(/(^|[^*])\*([^*]+)\*/g, "$1<em>$2</em>")
      .replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2" target="_blank" rel="noopener">$1</a>');

    const lines = md.split(/\r?\n/);
    const out = [];
    let i = 0;

    while (i < lines.length) {
      const ln = lines[i];

      // fenced code
      if (/^```/.test(ln)) {
        const buf = [];
        i++;
        while (i < lines.length && !/^```/.test(lines[i])) { buf.push(esc(lines[i])); i++; }
        i++; // consume closing fence
        out.push(`<pre><code>${buf.join("\n")}</code></pre>`);
        continue;
      }

      // horizontal rule
      if (/^\s*---+\s*$/.test(ln)) { out.push("<hr/>"); i++; continue; }

      // heading
      const h = ln.match(/^(#{1,6})\s+(.*)$/);
      if (h) { const n = h[1].length; out.push(`<h${n}>${inline(esc(h[2]))}</h${n}>`); i++; continue; }

      // blockquote
      if (/^>\s?/.test(ln)) {
        const buf = [];
        while (i < lines.length && /^>\s?/.test(lines[i])) { buf.push(lines[i].replace(/^>\s?/, "")); i++; }
        out.push(`<blockquote>${inline(esc(buf.join(" ")))}</blockquote>`);
        continue;
      }

      // table (very light detection: a line then a --- separator)
      if (/^\s*\|.*\|\s*$/.test(ln) && i + 1 < lines.length && /^\s*\|[\s\-:|]+\|\s*$/.test(lines[i+1])) {
        const headers = ln.trim().slice(1, -1).split("|").map(s => s.trim());
        i += 2;
        const rows = [];
        while (i < lines.length && /^\s*\|.*\|\s*$/.test(lines[i])) {
          rows.push(lines[i].trim().slice(1, -1).split("|").map(s => s.trim()));
          i++;
        }
        out.push(
          "<table><thead><tr>" +
          headers.map(h => `<th>${inline(esc(h))}</th>`).join("") +
          "</tr></thead><tbody>" +
          rows.map(r => "<tr>" + r.map(c => `<td>${inline(esc(c))}</td>`).join("") + "</tr>").join("") +
          "</tbody></table>"
        );
        continue;
      }

      // unordered list
      if (/^\s*[-*]\s+/.test(ln)) {
        const buf = [];
        while (i < lines.length && /^\s*[-*]\s+/.test(lines[i])) {
          buf.push(lines[i].replace(/^\s*[-*]\s+/, ""));
          i++;
        }
        out.push("<ul>" + buf.map(x => `<li>${inline(esc(x))}</li>`).join("") + "</ul>");
        continue;
      }

      // ordered list
      if (/^\s*\d+\.\s+/.test(ln)) {
        const buf = [];
        while (i < lines.length && /^\s*\d+\.\s+/.test(lines[i])) {
          buf.push(lines[i].replace(/^\s*\d+\.\s+/, ""));
          i++;
        }
        out.push("<ol>" + buf.map(x => `<li>${inline(esc(x))}</li>`).join("") + "</ol>");
        continue;
      }

      // blank line -> paragraph break
      if (/^\s*$/.test(ln)) { i++; continue; }

      // paragraph (collect until blank / block)
      const buf = [];
      while (i < lines.length && !/^\s*$/.test(lines[i]) && !/^(#{1,6}\s|>\s?|```|[-*]\s|\d+\.\s|\s*---+\s*$)/.test(lines[i])) {
        buf.push(lines[i]);
        i++;
      }
      if (buf.length) out.push(`<p>${inline(esc(buf.join(" ")))}</p>`);
    }

    return out.join("\n");
  }

  // ---------- generate brief ----------
  async function generateBrief() {
    const btn    = document.getElementById("generate-brief-btn");
    const status = document.getElementById("generate-status");

    btn.disabled = true;
    const setStatus = (msg) => {
      status.className = "generate-status";
      status.innerHTML = `<div class="spinner"></div> ${msg}`;
    };
    setStatus("Generating — this takes 30–60 seconds…");

    try {
      await window.macroBrief.generateBrief();
      // brief-generated IPC event triggers reloadAfterGeneration
    } catch (e) {
      status.className   = "generate-status error";
      status.textContent = "Error: " + (e.message || String(e));
      btn.disabled = false;
    }
  }

  // Live status updates from main process (e.g. retry countdown)
  if (window.macroBrief?.onBriefStatus) {
    window.macroBrief.onBriefStatus((msg) => {
      const status = document.getElementById("generate-status");
      if (status && status.classList.contains("generate-status") && !status.classList.contains("error") && !status.classList.contains("ok")) {
        status.innerHTML = `<div class="spinner"></div> ${msg}`;
      }
    });
  }

  async function reloadAfterGeneration(result) {
    const btn    = document.getElementById("generate-brief-btn");
    const status = document.getElementById("generate-status");

    // Reload briefs index and chart data
    try {
      const [history, flows, dailyFlows] = await Promise.all([
        loadJSON("data/history.json"),
        loadJSON("data/flows.json"),
        loadDailyFlows(),
      ]);
      state.history    = history;
      state.flows      = flows;
      state.dailyFlows = dailyFlows;
      window.Chart.init({ history, flows, dailyFlows });
    } catch {}

    try {
      state.briefs = await loadJSON("briefs/index.json");
      renderBriefHistory();
      if (result) {
        const b = state.briefs.briefs.find(x => x.slug === result.slug);
        if (b) loadBrief(b);
      } else {
        loadLatestBrief();
      }
    } catch {}

    status.className   = "generate-status ok";
    status.textContent = "Brief generated ✓";
    btn.disabled = false;
    setTimeout(() => { if (status.textContent === "Brief generated ✓") status.textContent = ""; }, 4000);
  }

  // ---------- bootstrap ----------
  document.addEventListener("DOMContentLoaded", () => {
    initTabs();
    document.getElementById("refresh-live").addEventListener("click", refreshLive);

    const genBtn = document.getElementById("generate-brief-btn");
    if (genBtn) {
      if (window.macroBrief && window.macroBrief.isElectron) {
        genBtn.addEventListener("click", generateBrief);
        window.macroBrief.onBriefGenerated((result) => reloadAfterGeneration(result));
      } else {
        genBtn.hidden = true;
      }
    }

    loadAll();
  });
})();
